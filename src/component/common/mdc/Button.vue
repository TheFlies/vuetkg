
<script>
import { propsToClasses } from './utils'
export default {
  functional: true,
  props: {
    dense: Boolean,
    raised: Boolean,
    compact: Boolean,
    primary: Boolean,
    accent: Boolean,
    themeDark: Boolean
  },
  render (h, { data, props, children }) {
    const staticClass = propsToClasses('mdc-button', props)
    data.staticClass = data.staticClass
      ? `${data.staticClass} ${staticClass}`
      : staticClass
    return h('button', {
      ...data
    }, children)
  }
}
</script>

